    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome, bairro, cidade, nascimento = null, sexo, cpf;
        long telefone;
        boolean validacao = true;

        if (jTextFieldCliente.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o Campo Cliente ! ");
            jTextFieldCliente.requestFocus();
        } else if (jTextFieldCpf.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o Campo CPF ! ");
            jTextFieldCpf.requestFocus();
        } else if (jTextFieldBairro.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o Campo Bairro ! ");
            jTextFieldBairro.requestFocus();
        } else if (jTextFieldCidade.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o Campo Cidade ! ");
            jTextFieldCidade.requestFocus();
        } else if (jTextFieldTelefone.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o Campo Telefone ! ");
            jTextFieldTelefone.requestFocus();
        } else if (!jRadioButtonMasc.isSelected() && !jRadioButtonFem.isSelected()) {
            JOptionPane.showMessageDialog(rootPane, "Escolha Seu Sexo ! ");
        } else {
            try {
                Date data = jDateChooserNascimento.getDate();
                nascimento = formatador.format(data);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Escolha a Data de Nascimento");
                validacao = false;
            }
            if (validacao == true) {
                nome = jTextFieldCliente.getText();
                cpf = jTextFieldCpf.getText();
                bairro = jTextFieldBairro.getText();
                cidade = jTextFieldCidade.getText();
                telefone = Long.parseLong(jTextFieldTelefone.getText());
                if (jRadioButtonMasc.isSelected()) {
                    sexo = "m";
                } else {
                    sexo = "f";
                }
                cliente.setNome(nome);
                cliente.setCpf(cpf);
                cliente.setBairro(bairro);
                cliente.setCidade(cidade);
                cliente.setTelefone(telefone);
                cliente.setSexo(sexo);
                cliente.setNascimento(nascimento);

                try {
                    clienteControle.cadastrarCliente(cliente);
                } catch (Exception ex) {
                    Logger.getLogger(FormClientesListar.class.getName()).log(Level.SEVERE, null, ex);
                }

                JOptionPane.showMessageDialog(rootPane, "Cadastrado");
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

