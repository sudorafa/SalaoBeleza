    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
        String login, senha, msg = null;
        login = jTFLogin.getText();
        senha = new String(jPFSenha.getPassword());
        boolean acesso = true;

        if (login.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe o Login");
            jTFLogin.requestFocus();
        } else if (senha.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe a Senha");
            jPFSenha.requestFocus();
        } else {
            Admin admin = new Admin();
            admin.setLogin(login);
            admin.setSenhaAtual(senha);

            try {
                DAdmin dAdmin = new DAdmin();
                dAdmin.validarLogin(admin);
            } catch (Exception ex) {
                //login invalido
                msg = ex.getMessage();
                acesso = false;
                jTFLogin.requestFocus();
            }

            if (acesso == true) {
                JOptionPane.showMessageDialog(rootPane, "Acesso OK  !");
                dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, msg);
            }
        }
    }//GEN-LAST:event_jButtonOKActionPerformed

