    private void jMenuItem6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem6ActionPerformed

        ConexaoSQLServer conexao = new ConexaoSQLServer();
        conexao.connect();

        if (conexao.connect() == true) {
            JOptionPane.showMessageDialog(null, "Conexão realizada com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Conexão não realizada!\n\nErro: " + conexao.sqle.getMessage() + "\n" + conexao.cnfe);
        }
        conexao.disconnect();
        if (conexao.disconnect() == true) {
            JOptionPane.showMessageDialog(null, "Conexão finalizada com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Conexão não realizada!\n\nErro: " + conexao.sqle.getMessage());
        }
    }//GEN-LAST:event_jMenuItem6ActionPerformed

